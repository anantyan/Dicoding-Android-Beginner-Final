package com.google.gallerymotogp.Component;

public class RiderComponent {

    String nameRider;
    String descriptionRider;
    String photoRider;
    String gelarRider;

    public String getNameRider() {
        return nameRider;
    }

    public void setNameRider(String nameRider) {
        this.nameRider = nameRider;
    }

    public String getDescriptionRider() {
        return descriptionRider;
    }

    public void setDescriptionRider(String descriptionRider) {
        this.descriptionRider = descriptionRider;
    }

    public String getPhotoRider() {
        return photoRider;
    }

    public void setPhotoRider(String photoRider) {
        this.photoRider = photoRider;
    }

    public String getGelarRider() {
        return gelarRider;
    }

    public void setGelarRider(String gelarRider) {
        this.gelarRider = gelarRider;
    }
}
